# this class is used to store a x/y or a width/height
# vector and allows to easier use and prevents bugs
# class is self-documenting and will not be further commented
class vector2:
    def __init__(self, xw, yh):
        self.x = xw
        self.y = yh

    def width():
        return self.x

    def height():
        return self.y

    def w():
        return width()

    def h():
        return height()

    def x():
        return self.x

    def y():
        return self.y
