


class vector2:
    def __init__(self, xw, yh):
        self.x = xw
        self.y = yh

    def width():
        return self.x

    def height():
        return self.y

    def w():
        return width()

    def h():
        return height()

    def x():
        return self.x

    def y():
        return self.y
